
public class complementary {
	public static void main(String[] args) {
		int x=-11;
		int y=-12;
		System.out.println(~x);//10
		System.out.println(~~x);//-11
		System.out.println(~~~x);//10
		System.out.println("................");
		System.out.println(~y);//11
		System.out.println(~~y);//-12
	}

}
